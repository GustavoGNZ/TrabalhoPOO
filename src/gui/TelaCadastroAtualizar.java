    private void BAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BAtualizarActionPerformed

        
        String senha, confirmarSenha;
        senha = String.valueOf(PSenha.getPassword());
        confirmarSenha = String.valueOf(PCSenha.getPassword());
        
        if (senha.equals(confirmarSenha)){
            Usuario user = new Usuario();
            user.setEmail(TEmail.getText());
            
            
            String idString = TIDUsuario.getText();
            int idUser = 0;
            idUser = Integer.parseInt(idString);
            
            user.setSenha(senha);
            user.setID_Usuario(idUser);
            
            UsuarioDAO userDAO = new UsuarioDAO();
            
            userDAO.atualizarUsuario(user);
            
        } else {
            JOptionPane.showMessageDialog(null, "Senha e confirmar senha n√£o conferem!");
        }
        
        TelaInicial tInicial = new TelaInicial();
        tInicial.setVisible(true);
        this.dispose();
        
    }//GEN-LAST:event_BAtualizarActionPerformed

